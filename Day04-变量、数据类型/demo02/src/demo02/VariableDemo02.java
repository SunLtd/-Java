public class VariableDemo02 {

	//进阶案例
	public static void main(String[] args) {
		//定义int类型变量x，并赋初始值100
		int x = 100;
		//打印该变量的值，观察是否为100
		System.out.println("x原值="+x);

		//重新给x变量赋值为200，改变x的值
		x = 200;
		//打印该变量的值，观察是否为200
		System.out.println("x新值="+x);
	}

}
